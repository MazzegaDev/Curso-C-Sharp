    Modificadores de acesso

    Public -> propria classe, subclasse no assembly, subclasse fora do assembly, classes fora do assembly

    Protected internal -> propria classe, subclasse no assembly, subclasse fora do assembly

    Internal -> propria classe, subclasse no assembly

    Protected -> propria classe, subclasses no assembly subclasse fora do assembly

    Private protected -> propria classe, subclasse no assembly

    Private -> propria classe

    Classes

    Acesso por qualquer classe
    -> public class Product

    Acesso somente dentro do assembly
    -> internal class Product
    -> class Product

    Acesso somente pela classe-mãe
    -> private class Product
    -> Nota: classe aninhada, (classe dentro de outra classe, por padrao e private)

